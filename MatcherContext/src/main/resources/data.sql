CREATE TABLE orderlines (
                            id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                            client VARCHAR(255),
                            ordernummer_edi VARCHAR(255),
                            refo_edi VARCHAR(255),
                            orderdatum DATE,
                            name_1 VARCHAR(255),
                            qty_ord INT,
                            batch VARCHAR(255),
                            line_alfa VARCHAR(255),
                            stock_inventory VARCHAR(255),
                            desc_1 VARCHAR(255),
                            pack_code VARCHAR(255),
                            product_code VARCHAR(255),
                            omschrijving VARCHAR(255),
                            product_extention_1 VARCHAR(255),
                            omschrijving_verpak VARCHAR(255),
                            pallet_size VARCHAR(255),
                            salescode VARCHAR(255)
);